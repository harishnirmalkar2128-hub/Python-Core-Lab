#Strings and Operators

#Strings

first_name = "Harish"
last_name = "Nirmalkar"

full_name = first_name + " "  + last_name
print ("full_name: ",full_name)
print ("first_name: ", (first_name + " ") * 3)
print ("len(full_name): ", len(full_name))
print("full_name:\n ", full_name + "\n "* 3)



